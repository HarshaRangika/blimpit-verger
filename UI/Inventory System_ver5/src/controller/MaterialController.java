package controller;

/**
 *
 * @author harsha
 */
public class MaterialController {
    
    private int id;
    private String rmId;
    private int quantity;
    private String origin;
    private String grade;
    private String packing;
    private int storagePortion;
    private String buyingVoucherPicture;
    private String productID;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the rmId
     */
    public String getRmId() {
        return rmId;
    }

    /**
     * @param rmId the rmId to set
     */
    public void setRmId(String rmId) {
        this.rmId = rmId;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    /**
     * @return the origin
     */
    public String getOrigin() {
        return origin;
    }

    /**
     * @param origin the origin to set
     */
    public void setOrigin(String origin) {
        this.origin = origin;
    }

    /**
     * @return the grade
     */
    public String getGrade() {
        return grade;
    }

    /**
     * @param grade the grade to set
     */
    public void setGrade(String grade) {
        this.grade = grade;
    }

    /**
     * @return the packing
     */
    public String getPacking() {
        return packing;
    }

    /**
     * @param packing the packing to set
     */
    public void setPacking(String packing) {
        this.packing = packing;
    }

    /**
     * @return the storagePortion
     */
    public int getStoragePortion() {
        return storagePortion;
    }

    /**
     * @param storagePortion the storagePortion to set
     */
    public void setStoragePortion(int storagePortion) {
        this.storagePortion = storagePortion;
    }

    /**
     * @return the buyingVoucherPicture
     */
    public String getBuyingVoucherPicture() {
        return buyingVoucherPicture;
    }

    /**
     * @param buyingVoucherPicture the buyingVoucherPicture to set
     */
    public void setBuyingVoucherPicture(String buyingVoucherPicture) {
        this.buyingVoucherPicture = buyingVoucherPicture;
    }

    /**
     * @return the productID
     */
    public String getProductID() {
        return productID;
    }

    /**
     * @param productID the productID to set
     */
    public void setProductID(String productID) {
        this.productID = productID;
    }
    
    
    
}

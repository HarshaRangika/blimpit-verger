package controller;

/**
 *
 * @author harsha
 */
public class StockController {
    
    private int id;
    private String batchNo;
    private int quantuty;
    private String rmTracking;
    private String productionDate;
    private String becoming;
    private String productID;
    private String document;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the batchNo
     */
    public String getBatchNo() {
        return batchNo;
    }

    /**
     * @param batchNo the batchNo to set
     */
    public void setBatchNo(String batchNo) {
        this.batchNo = batchNo;
    }

    /**
     * @return the quantuty
     */
    public int getQuantuty() {
        return quantuty;
    }

    /**
     * @param quantuty the quantuty to set
     */
    public void setQuantuty(int quantuty) {
        this.quantuty = quantuty;
    }

    /**
     * @return the rmTracking
     */
    public String getRmTracking() {
        return rmTracking;
    }

    /**
     * @param rmTracking the rmTracking to set
     */
    public void setRmTracking(String rmTracking) {
        this.rmTracking = rmTracking;
    }

    /**
     * @return the productionDate
     */
    public String getProductionDate() {
        return productionDate;
    }

    /**
     * @param productionDate the productionDate to set
     */
    public void setProductionDate(String productionDate) {
        this.productionDate = productionDate;
    }

    /**
     * @return the becoming
     */
    public String getBecoming() {
        return becoming;
    }

    /**
     * @param becoming the becoming to set
     */
    public void setBecoming(String becoming) {
        this.becoming = becoming;
    }

    /**
     * @return the productID
     */
    public String getProductID() {
        return productID;
    }

    /**
     * @param productID the productID to set
     */
    public void setProductID(String productID) {
        this.productID = productID;
    }

    /**
     * @return the document
     */
    public String getDocument() {
        return document;
    }

    /**
     * @param document the document to set
     */
    public void setDocument(String document) {
        this.document = document;
    }
    
    
    
}
